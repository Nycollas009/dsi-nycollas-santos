public class ExplicaçãoAula06 {
    public static void main(String[] args) {
    int a = 5;
    int b = 3;
    System.out.println(a == b); // returns false because 5 is not equal to 3
  

    int c = 5;
    int d = 3;
    System.out.println(c != d); // returns true because 5 is not equal to 3
  }
}
